name = "Om Mane"

print(name)

print(len(name))        # length of string

nameshort = name[0:5]   # start from index 0 all the way till 5 (excluding 5)

print(nameshort)

character = name[1]     # prints the index of 1

print(character)

print("---------------------------------------------------------")
print("Negative slicing")

print(name[-4:-1])

print(name[:4])     # from [0:4]
print(name[1:])     # from [1:0]

print("---------------------------------------------------------")

word = "0123456789"
print(word[1:7:3])

print("---------------------------------------------------------")

